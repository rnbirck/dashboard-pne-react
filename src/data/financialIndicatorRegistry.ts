export interface FinancialIndicatorDefinition {
  id: string
  publicName: string
  shortName: string
  question: string
  importance: string
  definition: string
  unit: 'BRL' | 'percent' | 'count' | 'coefficient' | 'BRL_per_student' | 'index' | 'boolean'
  domain: FinancialIndicatorDomain
  secondaryDomains: readonly FinancialIndicatorDomain[]
  stage: FinancialIndicatorStage
  nature: FinancialIndicatorNature
  source: string
  originFields: readonly string[]
  formula: string
  roundingRule: string
  nullRule: string
  zeroMeaning: string
  periodType: string
  yearComparability: string
  aggregationRule: FinancialUseDecision
  perStudentRule: FinancialUseDecision
  municipalComparisonRule: FinancialUseDecision
  roles: readonly FinancialIndicatorRole[]
  aliases: readonly string[]
  canonicalOwner: 'panorama' | 'siope' | 'fundeb' | 'vaar' | 'pnate'
  requiresReconciliation: boolean
  doubleCountingRisk: {
    level: DoubleCountingRiskLevel
    note: string
  }
  methodologyNote: string
  recommendedContext: string
  coverageNote: string
  equivalentOrOverlappingFields: readonly string[]
}

